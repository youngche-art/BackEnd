with open('data.json', 'r') as f:
    data_list = json.load(f)

    new_list = []
    for item in data_list:
        new_data = {"model": "pictures.picture"}
        
     # "색" 필드 변환
    field_name = "색"
    if field_name in item:
        field_value = item[field_name]
        if isinstance(field_value, str):
            try:
                # 문자열을 리스트로 변환하여 각 장르를 해당하는 인덱스로 매핑
                genre_list_str = field_value.strip('[]').split(', ')
                genre_int_list = [genre_list.index(genre.strip('"')) + 1 for genre in genre_list_str]
                item[field_name] = genre_int_list
            except ValueError as e:
                print(f"Error parsing genre list in '{field_name}': {e}")
                item[field_name] = []
        else:
            print(f"Error: '{field_name}' value is not a string")
    else:
        print(f"Warning: '{field_name}' key not found")
        item[field_name] = []
    
    # "형태" 필드 변환
    if "형태" in item:
        if isinstance(item["형태"], list):
            genre_int_list_b = []
            for genre in item["형태"]:
                if genre in genre_list_b:
                    genre_init_b = genre_list_b.index(genre) + 1
                    genre_int_list_b.append(genre_init_b)
                else:
                    print(f"Genre '{genre}' is not in genre_list_b")
            item["형태"] = genre_int_list_b
        else:
            print("Error: '형태' value is not a list")
    else:
        print("Warning: '형태' key not found")
        item["형태"] = []
    

    # "형태" 필드 변환
    if "마음" in item:
        if isinstance(item["마음"], list):
            genre_int_list_c = []
            for genre in item["마음"]:
                if genre in genre_list_c:
                    genre_init_c = genre_list_c.index(genre) + 1
                    genre_int_list_c.append(genre_init_c)
                else:
                    print(f"Genre '{genre}' is not in genre_list_b")
            item["마음"] = genre_int_list_c
        else:
            print("Error: '형태' value is not a list")
    else:
        print("Warning: '형태' key not found")
        item["마음"] = []
    
    new_data["fields"] = item
    new_list.append(new_data)

with open('eleven.json', 'w', encoding='UTF-8') as f:
    json.dump(new_list, f, ensure_ascii=False, indent=2)
